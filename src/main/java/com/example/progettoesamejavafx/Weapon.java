package com.example.progettoesamejavafx;

public class Weapon {
    String rarity;
    String calibre;
    String mimetic;

    public String getRarity() {
        return rarity;
    }

    public void setRarity(String rarity) {
        this.rarity = rarity;
    }

    public String getCalibre() {
        return calibre;
    }

    public void setCalibre(String calibre) {
        this.calibre = calibre;
    }

    public String getMimetic() {
        return mimetic;
    }

    public void setMimetic(String mimetic) {
        this.mimetic = mimetic;
    }

}
